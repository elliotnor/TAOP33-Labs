@constraint(SUDO, row[r in 1:9, k in 1:9], sum(x[r,c,k] for c in 1:9) == 1) #Rows

@constraint(SUDO, column[c in 1:9, k in 1:9], sum(x[r,c,k] for r in 1:9) == 1) #Columns

@constraint(SUDO, square[c in 1:3:7, r in 1:3:7, k in 1:9], sum(x[i,j,k] for i in c:(c+2), j in r:(r+2)) == 1) #Squares

@constraint(SUDO, num[i in 1:9, j in 1:9], sum(x[i, j, k] for k in 1:9) == 1) #Number

@constraint(SUDO, fix[i in 1:9, j in 1:9; given[i,j] > 0], x[i, j, given[i, j]] == 1 ) #Data
